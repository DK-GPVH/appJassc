class ListPropiedad {
  String id;
  String manzana;
  String lote;
  String zona;
  String nrodesuministro;
  String cliente;
  String categoria;
  String estado;
  String fechains;
  String cliente_dni;

  ListPropiedad(
      this.id,
      this.manzana,
      this.lote,
      this.zona,
      this.nrodesuministro,
      this.cliente,
      this.categoria,
      this.estado,
      this.fechains,
      this.cliente_dni) {
    this.id = id;
    this.manzana = manzana;
    this.lote = lote;
    this.zona = zona;
    this.nrodesuministro = nrodesuministro;
    this.cliente = cliente;
    this.categoria = categoria;
    this.estado = estado;
    this.fechains = fechains;
    this.cliente_dni = cliente_dni;
  }
}
