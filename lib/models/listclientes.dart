class ListCliente {
  String id;
  String nombre;
  String apellidop;
  String apellidom;
  String fechanac;
  String tipo_documento_id;
  String nrodocumento;
  String telefono;

  ListCliente(this.id, this.nombre, this.apellidop, this.apellidom,
      this.fechanac, this.tipo_documento_id, this.nrodocumento, this.telefono) {
    this.id = id;
    this.nombre = nombre;
    this.apellidop = apellidop;
    this.apellidom = apellidom;
    this.fechanac = fechanac;
    this.tipo_documento_id = tipo_documento_id;
    this.nrodocumento = nrodocumento;
    this.telefono = telefono;
  }
}
