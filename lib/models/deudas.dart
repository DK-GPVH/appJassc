class Deudas {
  String id;
  String manzana;
  String lote;
  String zona;
  String categoria;
  String monto;
  String fecha;
  String propiedadId;

  Deudas(this.id, this.manzana, this.lote, this.zona, this.categoria,
      this.monto, this.fecha, this.propiedadId) {
    this.id = id;
    this.manzana = manzana;
    this.lote = lote;
    this.zona = zona;
    this.categoria = categoria;
    this.monto = monto;
    this.fecha = fecha;
    this.propiedadId = propiedadId;
  }
}
